    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linhaSelecionada = tabelaProdutos.getSelectedRow();
        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(null, "Selecione uma linha antes de deletar");
        } else {
        DefaultTableModel modelo = (DefaultTableModel) tabelaProdutos.getModel();
        
        //int codigo = Integer.parseInt(modelo.getValueAt(linhaSelecionada, 0).toString());
        /*int[] selectedRows = tabelaProdutos.getSelectedRows(); 
        for (int i = 0; i < selectedRows.length; i++) {
        System.out.println(tabelaProdutos.getValueAt(selectedRows[i], 0).toString());
        }*/
        int codigo = Integer.parseInt(modelo.getValueAt(linhaSelecionada, 0).toString());
        
        //Chamar a DAO no método excluir
        boolean retorno = ProdutosDAO.deletarProduto(codigo);
        
        if(retorno){
            JOptionPane.showMessageDialog(rootPane, "Produto exluido com sucesso!");
        }else{
            JOptionPane.showMessageDialog(rootPane, "Falha ao excluir produto!");
        }
        }
        
        //readTabela();
    }//GEN-LAST:event_btnExcluirActionPerformed


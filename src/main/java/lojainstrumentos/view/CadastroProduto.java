    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed

        if (nome_produto.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Campo produto nao pode estar vazio!");
            return;
        }

        if (campo_quantidade.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Campo quantidade nao pode estar vazio!");
            return;
        }

        if (cod_produto.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Campo código produto nao pode estar vazio!");
            return;
        }

        if (descricao_produto.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Campo descrição nao pode estar vazio!");
            return;
        }
        if (preco_produto.getText().replace(",", "").replace("R$", "").trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Campo preço nao pode estar vazio!");
            return;
        }

        Produto objAlterar = new Produto();

        objAlterar.setNomeProduto(nome_produto.getText());
        objAlterar.setDescricaoProd(descricao_produto.getText());
        objAlterar.setPreco(Double.parseDouble(preco_produto.getText().replace(",", ".").replace("R$", "")));
        objAlterar.setQuantidadeProduto(Integer.parseInt(campo_quantidade.getText()));
        objAlterar.setCodigoProduto(Integer.parseInt(cod_produto.getText()));

        //Chama a DAO
        boolean retorno = ProdutosDAO.atualizarProduto(objAlterar);

        if (retorno) {
            JOptionPane.showMessageDialog(rootPane, "Atualizado com sucesso!");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Erro ao atualizar!");
        }
    }//GEN-LAST:event_btnAlterarActionPerformed


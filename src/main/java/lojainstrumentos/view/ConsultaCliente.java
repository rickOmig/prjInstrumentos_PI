    public void readCliente(String cpf) {
        //Chama DAO para consulta
        ArrayList<Cliente> lista = ClientesDAO.pesquisarCliente(cpf);

        DefaultTableModel modelo = (DefaultTableModel) tabelaClientes.getModel();

        //Zera tabela
        modelo.setRowCount(0);

        //Adicionar linhas com for
        for (Cliente item : ClientesDAO.pesquisarCliente(cpf)) {
            modelo.addRow(new String[]{
                String.valueOf(item.getIdCliente()),
                String.valueOf(item.getNome()),
                String.valueOf(item.getCpf()),
                String.valueOf(item.getEndereco()),
                String.valueOf(item.getTelefone()),
                String.valueOf(item.getEmail()),
                String.valueOf(item.getGenero()),
                String.valueOf(item.getEstadoCivil()),
                String.valueOf(item.getDataNasc()),}
            );
        }
    }


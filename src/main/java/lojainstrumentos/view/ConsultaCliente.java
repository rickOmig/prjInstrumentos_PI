    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linhaSelecionada = tabelaClientes.getSelectedRow();
        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(null, "Selecione uma linha antes de deletar");
        } else {
            DefaultTableModel modelo = (DefaultTableModel) tabelaClientes.getModel();

            String cpf = modelo.getValueAt(linhaSelecionada, 1).toString();

            //Chamar a DAO no método excluir
            boolean retorno = ClientesDAO.deletarCliente(cpf);

            if (retorno) {
                JOptionPane.showMessageDialog(rootPane, "Cliente exluido com sucesso!");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Falha ao excluir cliente!");
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        int linhaSelecionada = tabelaClientes.getSelectedRow();

        DefaultTableModel modelo = (DefaultTableModel) tabelaClientes.getModel();
        int id = Integer.parseInt(modelo.getValueAt(linhaSelecionada, 0).toString());
        String nome = (modelo.getValueAt(linhaSelecionada, 1).toString());
        String cpf = (modelo.getValueAt(linhaSelecionada, 2).toString());
        String endereco = (modelo.getValueAt(linhaSelecionada, 3).toString());
        String telefone = (modelo.getValueAt(linhaSelecionada, 4).toString());
        String email = (modelo.getValueAt(linhaSelecionada, 5).toString());
        String nascimento = (modelo.getValueAt(linhaSelecionada, 8).toString());

        //Chamar a tela de Cadastro passando um obj
        Cliente obj = new Cliente(nome, endereco, email, nascimento, telefone, cpf, id);

        AlterarCliente novaTela = new AlterarCliente(obj);
        novaTela.setVisible(true);
    }//GEN-LAST:event_btnAlterarActionPerformed

